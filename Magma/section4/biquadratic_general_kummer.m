/*
    This file computes the biquadratics for the general kummer surface
*/

function CheckMatrix(PpQ, PmQ)
    // given the two points D = P - Q and S = P + Q 
    // returns the values of the matrix CM with CM_i,j = S_i*D_j - D_i * S_j
    S := Eltseq(PpQ);
    D := Eltseq(PmQ);

    Fp := Parent(PpQ[1]);

    CM := ZeroMatrix(Fp, 4,4);
    for i,j in [1..4] do
        CM[i,j] := S[i]*D[j] + D[i]*S[j];
    end for;
    
    return CM;
end function;

